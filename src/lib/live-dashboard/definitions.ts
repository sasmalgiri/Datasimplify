// ‚îÄ‚îÄ‚îÄ DYNAMIC DASHBOARD GENERATORS (for auto-generated 5000+ dashboards) ‚îÄ‚îÄ‚îÄ

function prettifySlug(slug: string): string {
  return slug
    .split(/[-_]+/)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(' ');
}

